package ryan.day5.annotation;

public class Person {
    @ValidatorAnnotation(max = 100)
    private int age;

    @ValidatorAnnotation(len = 2)
    private String name;

    public Person() {
    }

    public Person(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
